<p>
	   <b>SMK Elektronika Indonesia (SMK EI)</b> Bukittinggi didirikan oleh Yayasan Pendidikan Teknologi Indonesia (YPTI) Bukittinggi dengan Rekomendasi Pemerintah Daerah Tk. II Kodya Bukittinggi, tanggal 12 Februari 1993 dengan No.189/Bappeda-Bkt/II-1993. Pada tanggal 17 Juli 1993 dikeluarkan Surat Keputusan dari Kepala Kantor Wilayah Departemen Pendidikan dan Kebudayaan Propinsi Sumatera Barat dengan Nomor:KPTS.172.op.R.1993 tentang izin operasional penyelenggaraan pendidikan <b>SMK Elektronika Indonesia Bukittinggi</b> yang pada saat itu bernama SMK Telekomunikasi dan Komputer (SMK TELKOMP) Bukittinggi. 
</p>
<p>
	Pada awal Tahun Ajaran 1993/1994 dimulailah penerimaan siswa baru <b>SMK Elektronika Indonesia Bukittinggi</b> yang beralamat Jl. Prof. Hamka No.55 Bukittinggi (Gedung disewa) dengan bidang studi Elektronika, yang mempunyai satu program keahlian yaitu Elektronika Komunikasi. 
</p>
<p>
	Sejalan dengan perkembangan IPTEK, maka pada tahun 1999 dengan dikeluarkannya  kurikulum edisi 99 berubahlah program keahlian yang ada di <b>SMK Elektronika Indonesia Bukittinggi</b> menjadi program keahlian Teknik Audio Video. Pada tahun 2009 dengan adanya spektrum baru maka SMK Elektronika Indonesia Bukittinggi menyesuaikan dan membuka Bidang Studi Keahlian Teknologi Informasi Dan Komunikasi, Program Studi Keahlian Teknik Komputer dan Informatika, dengan Kompetensi Keahlian Teknik Komputer dan Jaringan (TKJ). 
</p>
<p>
	Berdasarkan hal tersebut diatas dan sejalan dengan perkembangan IPTEK, maka <b>SMK Elektronika Indonesia Bukittinggi</b> tahun 1993 sampai sekarang selalu berupaya mengembangkan kurikulum sesuai dengan kebutuhan dunia usaha dan industri.
</p>
