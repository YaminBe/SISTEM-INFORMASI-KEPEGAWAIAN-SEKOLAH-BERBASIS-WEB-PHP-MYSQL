<b>VISI DAN MISI</b>
<p><b>Visi </b> <br>
	Menjadi SMK Yang Unggul Dalam IPTEK dan IMTAQ.
 </p> 

<p>
	<b>Misi</b>
<p>1)	Mendidik tenaga kerja yang beriman dan bertaqwa, disiplin, mandiri dan beretos kerja tinggi.</p>
<p>2)	Menyelenggarakan pendidikan dan pelatihan yang profesional di Bidang Teknik Komputer dan Jaringan.</p>
<p>3)	Menghasilkan tamatan yang menguasai IPTEK yang mampu bersaing di du/di baik tingkat Regional, Nasional maupun Internasional.</p>
